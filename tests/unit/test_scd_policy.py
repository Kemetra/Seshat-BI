def test_hr2_ambiguous_migration_message_has_no_numeric_tally(tmp_path: Path) -> None:
    """FR-013: no message may report a raw finding/file count (hard rule #9),
    including the ambiguous-migration-set branch (a candidate leak point since
    it must name multiple files)."""
    yaml_text = (
        "gold_star:\n  dimensions:\n    - name: gold.dim_widget\n"
        "      scd_type: type_2\n"
    )
    map_rel = _map(tmp_path, "widgets_tally", yaml_text)
    mig1 = _mig(tmp_path, "widgets_tally", _DDL_INSERT_DROP_REBUILD)
    mig2_rel = "warehouse/migrations/0002_create_gold_widgets_tally_star.sql"
    (tmp_path / mig2_rel).write_text(_CTAS_DROP_REBUILD, encoding="utf-8")
    ctx = _ctx(tmp_path, map_rel, mig1, mig2_rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert not re.search(r"\b\d+\s+files?\b", findings[0].message)
