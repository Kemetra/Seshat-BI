# --- landing rule: both real committed maps are GRANDFATHERED (owner ruling
# 2026-07-05) -- they declare scd_type on no dimension and do not opt in, so HR2
# is <no-finding> on the committed tree while enforcing on every new/touched map ---


def test_hr2_grandfathers_the_real_committed_maps() -> None:
    """Both committed maps predate this feature and declare no scd_type on any
    gold_star.dimensions[] entry and do not set scd_enforced, so the grandfather
    ruling (2026-07-05) exempts them: HR2 emits NOTHING on the committed tree.
    Pins that HR2 lands <no-finding> (a future regression that made it fire
    retroactively on an already-approved map would be caught here)."""
    rels = [
        "mappings/retail_store_sales/source-map.yaml",
        "mappings/demo_sample_orders/source-map.yaml",
    ]
    tracked = tuple(rel for rel in rels if (_REPO / rel).exists())
    assert tracked, "expected fixture maps are missing from the repo"
    ctx = RuleContext(repo_root=_REPO, tracked_files=tracked)
    assert list(check_hr2(ctx)) == []


