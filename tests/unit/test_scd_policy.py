"""Unit tests for HR2 (declared SCD type per gold dimension)."""

from __future__ import annotations

import re
from pathlib import Path

import pytest

from retail.core import RuleContext, Severity
from retail.rules.scd_policy import check_hr2

pytestmark = pytest.mark.unit

_REPO = Path(__file__).resolve().parents[2]


def _map(tmp_path: Path, table: str, yaml_text: str) -> str:
    """Write a source-map.yaml under tmp_path/mappings/<table>/; return its rel path."""
    rel = f"mappings/{table}/source-map.yaml"
    dest = tmp_path / rel
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(yaml_text, encoding="utf-8")
    return rel


def _mig(tmp_path: Path, table: str, sql: str) -> str:
    """Write a gold migration under tmp_path/warehouse/migrations/; return rel path."""
    rel = f"warehouse/migrations/0001_create_gold_{table}_star.sql"
    dest = tmp_path / rel
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(sql, encoding="utf-8")
    return rel


def _ctx(tmp_path: Path, *rel: str) -> RuleContext:
    return RuleContext(repo_root=tmp_path, tracked_files=tuple(rel))


_DDL_INSERT_DROP_REBUILD = """\
BEGIN;
DROP TABLE IF EXISTS gold.dim_widget;
DROP TABLE IF EXISTS gold.dim_other;

CREATE TABLE gold.dim_other (other_sk INT, other_name TEXT);
INSERT INTO gold.dim_other (other_name) SELECT other_name FROM silver.widgets;

CREATE TABLE gold.dim_widget (
  widget_sk INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  widget_name TEXT
);
INSERT INTO gold.dim_widget (widget_name)
SELECT widget_name FROM silver.widgets;
COMMIT;
"""

_CTAS_DROP_REBUILD = """\
BEGIN;
DROP TABLE IF EXISTS gold.dim_widget;
CREATE TABLE gold.dim_widget AS SELECT widget_name FROM silver.widgets;
COMMIT;
"""


# --- US1: valid declarations pass; invalid value fails closed ---


def test_hr2_valid_declarations_produce_no_finding(tmp_path: Path) -> None:
    yaml_text = (
        "gold_star:\n"
        "  dimensions:\n"
        "    - name: dim_a\n"
        "      scd_type: type_1\n"
        "    - name: dim_b\n"
        "      scd_type: type_2\n"
    )
    rel = _map(tmp_path, "t1", yaml_text)
    ctx = _ctx(tmp_path, rel)
    assert list(check_hr2(ctx)) == []


def test_hr2_invalid_value_fails_closed(tmp_path: Path) -> None:
    yaml_text = (
        "gold_star:\n  dimensions:\n    - name: dim_a\n      scd_type: quarterly\n"
    )
    rel = _map(tmp_path, "t2", yaml_text)
    ctx = _ctx(tmp_path, rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert findings[0].severity is Severity.ERROR
    assert findings[0].rule_id == "HR2"
    assert "dim_a" in findings[0].message
    assert "quarterly" in findings[0].message


def test_hr2_invalid_value_clears_when_corrected(tmp_path: Path) -> None:
    yaml_text = "gold_star:\n  dimensions:\n    - name: dim_a\n      scd_type: type_1\n"
    rel = _map(tmp_path, "t2b", yaml_text)
    ctx = _ctx(tmp_path, rel)
    assert list(check_hr2(ctx)) == []


# --- US2 (MVP): type_2 + drop-and-rebuild fails closed; type_1 never fires ---


def test_hr2_type2_ddl_insert_drop_rebuild_fails_closed(tmp_path: Path) -> None:
    yaml_text = (
        "gold_star:\n  dimensions:\n    - name: gold.dim_widget\n"
        "      scd_type: type_2\n"
    )
    map_rel = _map(tmp_path, "widgets", yaml_text)
    mig_rel = _mig(tmp_path, "widgets", _DDL_INSERT_DROP_REBUILD)
    ctx = _ctx(tmp_path, map_rel, mig_rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert findings[0].severity is Severity.ERROR
    assert findings[0].rule_id == "HR2"
    assert (
        "dim_widget" in findings[0].message or "gold.dim_widget" in findings[0].message
    )
    assert findings[0].locator == mig_rel


