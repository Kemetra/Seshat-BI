def test_hr2_ambiguous_migration_set_fails_closed(tmp_path: Path) -> None:
    yaml_text = (
        "gold_star:\n  dimensions:\n    - name: gold.dim_widget\n"
        "      scd_type: type_2\n"
    )
    map_rel = _map(tmp_path, "widgets_dup", yaml_text)
    mig1 = _mig(tmp_path, "widgets_dup", _DDL_INSERT_DROP_REBUILD)
    # a second, differently-prefixed file matching the same glob
    mig2_rel = "warehouse/migrations/0002_create_gold_widgets_dup_star.sql"
    (tmp_path / mig2_rel).write_text(_CTAS_DROP_REBUILD, encoding="utf-8")
    ctx = _ctx(tmp_path, map_rel, mig1, mig2_rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert findings[0].severity is Severity.ERROR
    assert "widgets_dup" in findings[0].message


# --- US3: undeclared scd_type is a Needs-decision, never a silent default ---


def test_hr2_missing_key_is_needs_decision(tmp_path: Path) -> None:
    # scd_enforced: true opts the map into adoption, so a missing declaration is
    # the Needs-decision ERROR (an un-adopted map would be grandfathered).
    yaml_text = "gold_star:\n  scd_enforced: true\n  dimensions:\n    - name: dim_a\n"
    rel = _map(tmp_path, "t3", yaml_text)
    ctx = _ctx(tmp_path, rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert findings[0].severity is Severity.ERROR
    assert "dim_a" in findings[0].message
    assert findings[0].locator == f"{rel}:dim_a"


def test_hr2_missing_key_is_grandfathered_on_unadopted_map(tmp_path: Path) -> None:
    """Owner ruling 2026-07-05: a map declaring scd_type on NO dimension and not
    opting in is grandfathered -- HR2 fires no missing-declaration ERROR."""
    yaml_text = "gold_star:\n  dimensions:\n    - name: dim_a\n    - name: dim_b\n"
    rel = _map(tmp_path, "t3_grandfathered", yaml_text)
    ctx = _ctx(tmp_path, rel)
    assert list(check_hr2(ctx)) == []


@pytest.mark.parametrize("placeholder", ['""', "null", "TBD", "tbd"])
def test_hr2_placeholder_values_are_needs_decision_not_invalid(
    tmp_path: Path, placeholder: str
) -> None:
    yaml_text = (
        "gold_star:\n  scd_enforced: true\n  dimensions:\n"
        f"    - name: dim_a\n      scd_type: {placeholder}\n"
    )
    safe_name = placeholder.strip('"').lower()
    rel = _map(tmp_path, f"t3b_{safe_name}", yaml_text)
    ctx = _ctx(tmp_path, rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 1
    assert findings[0].severity is Severity.ERROR
    # Needs-decision message, not an "invalid value" message
    assert "declare" in findings[0].message.lower()


def test_hr2_two_undeclared_dimensions_produce_two_findings(tmp_path: Path) -> None:
    # adopting map (scd_enforced) with two undeclared dims -> two ERRORs
    yaml_text = (
        "gold_star:\n  scd_enforced: true\n  dimensions:\n"
        "    - name: dim_a\n    - name: dim_b\n"
    )
    rel = _map(tmp_path, "t4", yaml_text)
    ctx = _ctx(tmp_path, rel)
    findings = list(check_hr2(ctx))
    assert len(findings) == 2
    names = {f.locator for f in findings}
    assert names == {f"{rel}:dim_a", f"{rel}:dim_b"}


